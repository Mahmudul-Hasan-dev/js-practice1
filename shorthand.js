// variable declare
var price1 = 20;
var price2 = 30;

// add to existing value

price1 = price1 + 20;

//print
console.log(price1);

//addition to same variable shorthand
//+= means adding some value to existing value
price1 += 60;
//print
console.log(price1);

// substraction shorthand

price2 = price2 - 5;

// shorthand for same variable
//-= means deduction from the existing value of same name variable
price2 -= 5;
//print
console.log(price2);

//multiplication shorthand
price2 *= 5;
//print
console.log(price2);

//division shorthand
price2 /= 2;
//print
console.log(price2);

var age = 25;
age = age + 1;
//++ means adding 1 to existing value
age++;
//-- means deduct 1 from existing value
age--;